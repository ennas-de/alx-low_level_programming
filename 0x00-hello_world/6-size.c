#include <stdio.h>

/** 
 * main - Entry point
 *
 * Return: Always 0 (Success)
 * */

int main() {
	char a;
	int b;
	long int c;
	long long int d;
	float e;

	printf("Size of a char: $c", a);
	printf("Size of an int: $d", b);
	printf("Size of a long int: $ld", c);
	printf("Size of a long long int: $ ld", d);
	printf("Size of a float: $f", e);

	return 0;
}

