#include <stdio.h>

/**
 * main - Entry point
 *
 * Return: Always 0 (Success)
*/

int main(void)
{
	char a;
	int b;
	long int c;
	long long int d;
	float e;

	printf("Size of a char: $c\n", a);
	printf("Size of an int: $d\n", b);
	printf("Size of a long int: $ld\n", c);
	printf("Size of a long long int: $ld\n", d);
	printf("Size of a float: $f\n", e);

	return (0);

}

